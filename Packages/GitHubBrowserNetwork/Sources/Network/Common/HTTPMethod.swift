import Foundation

/// Enum representing HTTP methods supported within EPI Networking.
public enum HTTPMethod: String {
    /// The GET HTTP method.
    case GET
    /// The PATCH HTTP method.
    case PATCH
    /// The POST HTTP method.
    case POST
    /// The PUT HTTP method.
    case PUT
    /// The DELETE HTTP method.
    case DELETE
}
